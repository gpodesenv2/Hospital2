    private void lstPacienteValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPacienteValueChanged
        // TODO add your handling code here:

        Agendamento a = (Agendamento) lstPaciente.getSelectedValue();

        txtPront.setText(a.getPaciente().getId().toString());
        txtNome.setText(a.getPaciente().getNome().toString());
        txtDataCirurgia.setDate(a.getDataInicio().getTime());
        txtCirurgia.setText(a.getProcedimento().toString());

        lstMaterials.removeAll();
        List l = new CirurgiaMedicamentoJpaController().getMedicamentos(a.getId().toString());
        lstMaterials.setListData(l.toArray());
    }//GEN-LAST:event_lstPacienteValueChanged

