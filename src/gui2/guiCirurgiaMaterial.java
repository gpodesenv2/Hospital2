    private void burcarAgendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_burcarAgendActionPerformed
        // TODO add your handling code here:
        paciente = new PacienteJpaController().getPaciente(txtPront.getText());

        txtNome.setText(paciente.getNome().toString());

        try {
            List l = new AgendamentoJpaController().getAgendamentoPac(txtPront.getText());
            lstPaciente.setListData(l.toArray());

            if (l.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Não foi agendada Cirurgia com este número de prontuário");
                txtNome.setText("");
                txtPront.setText("");
                txtDataCirurgia.setDate(null);
                txtCirurgia.setText("");
                lstPaciente.setModel(new DefaultListModel());
                lstMaterials.setModel(new DefaultListModel());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi agendada Cirurgia com este número de prontuário");
            txtNome.setText("");
            txtPront.setText("");
            txtDataCirurgia.setDate(null);
            txtCirurgia.setText("");
            lstPaciente.setModel(new DefaultListModel());
            lstMaterials.setModel(new DefaultListModel());
        }
    }//GEN-LAST:event_burcarAgendActionPerformed

    private void bntAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAdicionarActionPerformed

        CirurgiaMaterial cm = new CirurgiaMaterial();
        ag = (Agendamento) lstPaciente.getSelectedValue();

        cm.setAgendamento(ag);
        cm.setDataCirurgia(txtDataCirurgia.getCalendar());
        cm.setMaterial((Material) cboMateriais.getSelectedItem());
        cm.setPeriodo(txtPeriodo.getText());

        new CirurgiaMaterialJpaController().create(cm);
        JOptionPane.showMessageDialog(null, "Novo material adicionado a Cirurgia '" + ag.getProcedimento() + "'\nPaciente: " + ag.getPaciente().getNome().toString());
        lstMaterials.removeAll();
        List l = new CirurgiaMaterialJpaController().getMaterial(ag.getId().toString());
        lstMaterials.setListData(l.toArray());
        //lstMedicamento.setModel(new DefaultListModel());
    }//GEN-LAST:event_bntAdicionarActionPerformed

